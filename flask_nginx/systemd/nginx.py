def run_app(
    application_dir: str,
    bind: str,
    app: str = "app.app",
    *,
    asgi: bool = False,
    args: tuple[str, ...] = (),
) -> subprocess.Popen[bytes]:

    if asgi:
        assert bind.startswith("unix:"), bind
        bind = bind[len("unix:") :]
        exe = "uvicorn"

        cmd = [
            sys.executable,
            "-m",
            "uvicorn",
            "--proxy-headers",
            f"--uds={bind}",
            *args,
            app,
        ]

    else:
        exe = "gunicorn"
        cmd = [
            sys.executable,
            "-m",
            "gunicorn",
            "--access-logfile=-",
            "--error-logfile=-",
            "--bind",
            bind,
            *args,
            app,
        ]

    click.secho(
        f"starting {exe} in {topath(application_dir)}",
        fg="green",
        bold=True,
    )

    click.secho(" ".join(cmd), fg="green")
    # subprocess.run(cmd, cwd=application_dir, env=os.environ, check=True)
    return subprocess.Popen(cmd, cwd=application_dir, env=os.environ)


def patch_nginx_conf(oldfile: Path, newfile: Path) -> Path:
    newlines: list[str] = []
    active: bool = False
    with newfile.open("rt", encoding="utf-8") as fp:
        for line in fp:
            if line.lstrip().startswith("# >>> generated by footprint"):
                active = True
            elif line.lstrip().startswith("# <<< generated by footprint"):
                break
            elif active:
                newlines.append(line)
    if not newlines:
        return newfile
    nnewfile = newfile.parent / f"{newfile.stem}-patched{newfile.suffix}"
    certbot = "# managed by Certbot" in oldfile.read_text(encoding="utf-8")
    with nnewfile.open("wt", encoding="utf-8") as fp:
        patched = False
        active = False

        with oldfile.open("rt", encoding="utf-8") as olfp:
            for line in olfp:
                if line.lstrip().startswith("# >>> generated by footprint"):
                    active = True
                    fp.write(line)
                    for newline in newlines:
                        if certbot and newline.lstrip().startswith("listen"):
                            continue
                        fp.write(newline)
                    patched = True
                elif line.lstrip().startswith("# <<< generated by footprint"):
                    active = False
                    fp.write(line)
                elif not active:
                    fp.write(line)
    return nnewfile if patched else newfile


def nginx_install(nginxconf: str) -> str | None:
    import filecmp
    from ..config import get_config

    nginxfile = Path(nginxconf)

    Config = get_config()

    conf = nginxfile.name
    # Ubuntu, RHEL8
    for td in Config.nginx_dirs:
        targetd = Path(td)
        if targetd.is_dir():
            break
    else:
        raise RuntimeError("can't find nginx configuration directory")
    sudo = which("sudo")
    systemctl = which("systemctl")

    def sudocmd(*args: Any, check: bool = True) -> subprocess.CompletedProcess[bytes]:
        return subprocess.run([sudo] + [str(a) for a in args], check=check)

    def runcmd(*args: Any, check: bool = True) -> subprocess.CompletedProcess[bytes]:
        return subprocess.run([str(a) for a in args], check=check)

    def systemctlcmd(*args: Any, check: bool = True) -> int:
        return subprocess.run(
            [sudo, systemctl] + [str(a) for a in args],
            check=check,
        ).returncode

    conffile = targetd / conf

    exists = conffile.is_file()
    if not exists or not filecmp.cmp(conffile, nginxfile):
        bak = None
        if exists:
            bak = nginxfile.parent / f"{conffile.name}.bak"
            runcmd("cp", conffile, bak)
            # file might have been patched by certbot or other tools so we need to patch it
            nnginxfile = patch_nginx_conf(conffile, nginxfile)
            if filecmp.cmp(conffile, nnginxfile):
                click.secho(f"nginx file {conf} unchanged", fg="green")
                if bak is not None:
                    bak.unlink(missing_ok=True)
                return conf
            click.secho(f"warning: overwriting old {conf}", fg="yellow")
            sudocmd("cp", nnginxfile, conffile)
        else:
            sudocmd("cp", nginxfile, conffile)

        if sudocmd("nginx", "-t", check=False).returncode != 0:
            sudocmd("rm", conffile)
            if bak is not None:
                sudocmd("mv", bak, conffile)
            click.secho("nginx configuration faulty", fg="red", err=True)
            return None if bak is None else conf
        if bak is not None:
            bak.unlink(missing_ok=True)

        systemctlcmd("restart", "nginx")
    else:
        click.secho(f"nginx file {conf} unchanged", fg="green")
    return conf


def nginx_uninstall(nginxconf: str) -> None:
    from ..config import get_config

    Config = get_config()
    nginxfile = Path(nginxconf)
    conf = nginxfile.name
    if "." not in conf:
        conf += ".conf"
    sudo = which("sudo")
    systemctl = which("systemctl")

    def sudocmd(*args: str, check: bool = True) -> subprocess.CompletedProcess[bytes]:
        return subprocess.run([sudo] + list(args), check=check)

    def systemctlcmd(*args: str, check: bool = True) -> int:
        return subprocess.run([sudo, systemctl] + list(args), check=check).returncode

    for d in Config.nginx_dirs:
        fname = Path(d) / conf
        if fname.is_file():
            sudocmd("rm", str(fname))
            systemctlcmd("restart", "nginx.service")
            return

    click.secho(f"no nginx file {nginxfile}", fg="yellow", err=True)


