@config.command(name="nginx", help=NGINX_HELP)  # noqa: C901
@template_option
@config_options
@click.option("--ssl", is_flag=True, help="make it secure")
@click.option(
    "--no-static",
    is_flag=True,
    help="Don't try to introspect static files. (Useful for non-flask websites)",
)
@asgi_option
@click.option(
    "-x",
    "--404",
    "exclude_urls",
    type=click.Path(exists=True, dir_okay=False, file_okay=True),
    help="""list of urls to immediately return 404 for (one per line)""",
)
@click.option(
    "--exclusive",
    is_flag=True,
    help="""add a negative assertion to the known urls of the app. (Caution you can't add any new routes to the app after this)""",
)
@click.option(
    "-d",
    "--app-dir",
    "application_dir",
    type=click.Path(exists=True, dir_okay=True, file_okay=False),
    help="""location of repo or current directory""",
)
@click.argument("server_name")
@click.argument("params", nargs=-1)
def nginx_cmd(
    application_dir: str | None,
    server_name: str,
    template: str | None,
    asgi: bool,
    exclude_urls: str | None,
    params: list[str],
    no_check: bool,
    output: str | None,
    ssl: bool = False,
    no_static: bool = False,
    exclusive: bool = False,
) -> None:
    """Generate nginx config file.

    PARAMS are key=value arguments for the template.
    """
    # pylint: disable=line-too-long
    # see https://www.digitalocean.com/community/tutorials/how-to-serve-flask-applications-with-gunicorn-and-nginx-on-ubuntu-20-04
    # place this in /etc/systemd/system/
    params = list(params)
    if no_static:
        params.append("app=@none")

    urls = []
    if exclude_urls:
        with open(exclude_urls, encoding="utf-8") as fp:
            for line in fp:
                line = line.strip()
                if line and line.startswith("/"):
                    urls.append(line[1:].replace(".", r"\."))
    nginx(
        application_dir or ".",
        server_name,
        params,
        template_name=template,
        check=not no_check,
        output=output,
        ssl=ssl,
        asgi=asgi,
        extra_params={"exclude_urls": urls},
        exclusive=exclusive,
    )


@config.command(name="nginx-run-app")
@click.option("-p", "--port", default=5000, help="port to listen", show_default=True)
@click.option(
    "-x",
    "--no-start",
    "no_start_app",
    is_flag=True,
    help="don't start the website in background",
    show_default=True,
)
@click.option(
    "--entrypoint",
    help="web application entrypoint",
)
@asgi_option
@click.option("--browse", is_flag=True, help="open web application in browser")
@click.option(
    "-d",
    "--app-dir",
    "application_dir",
    type=click.Path(exists=True, dir_okay=True, file_okay=False),
    help="""location of repo or current directory""",
)
def nginx_run_app_cmd(
    application_dir: str | None,
    port: int,
    entrypoint: str | None,
    asgi: bool,
    no_start_app: bool = False,
    browse: bool = False,
) -> None:
    """Run nginx as a non daemon process with web app in background."""
    import signal
    import uuid
    from threading import Thread

    from tempfile import gettempdir

    from ..utils import browser, require_mod

    nginx_exe = which("nginx")

    if not no_start_app:
        if asgi:
            require_mod("uvicorn")
        else:
            require_mod("gunicorn")

    if application_dir is None:
        application_dir = "."

    application_dir = topath(application_dir)
    tmplt = get_template("nginx-test.conf", application_dir)
    res = tmplt.render(  # pylint: disable=no-member
        application_dir=application_dir,
        port=port,
    )
    tmpfile = Path(gettempdir()) / f"nginx-{uuid.uuid4()}.conf"

    url = f"http://127.0.0.1:{port}"
    click.secho(f"listening on {url}", fg="green", bold=True)
    running: subprocess.Popen[bytes] | None = None
    brws: Thread | None = None
    try:
        with tmpfile.open("wt", encoding="utf-8") as fp:
            fp.write(res)

        if not no_start_app:
            app_entry = entrypoint or get_app_entrypoint(application_dir, asgi=asgi)
            running = run_app(
                application_dir,
                f"unix:{application_dir}/app.sock",
                app_entry,
                asgi=asgi,
            )
        else:
            click.secho(
                f"expecting app running in {application_dir} with app.sock as unix socket",
                fg="magenta",
                bold=True,
            )
        if browse:
            brws = browser(url=url)

        subprocess.check_call([nginx_exe, "-c", str(tmpfile)])

    finally:
        tmpfile.unlink(missing_ok=True)
        if running is not None:
            os.kill(running.pid, signal.SIGINT)
        if brws is not None:
            brws.join()
        # os.system("stty sane")


@config.command(name="nginx-run")
@click.option(
    "-p",
    "--port",
    default=5000,
    help="port for nginx to listen",
)
@click.option(
    "--entrypoint",
    help="web application entrypoint",
)
@asgi_option
@click.option("--browse", is_flag=True, help="open web application in browser")
@click.argument("nginxfile", type=click.File("rt", encoding="utf-8"))
@click.option(
    "-d",
    "--app-dir",
    "application_dir",
    type=click.Path(exists=True, dir_okay=True, file_okay=False),
    help="""location of repo or current directory""",
)
@click.argument("server_args", nargs=-1)
def nginx_run_cmd(
    nginxfile: IO[str],
    application_dir: str | None,
    entrypoint: str | None,
    port: int,
    browse: bool,
    asgi: bool,
    server_args: tuple[str, ...],
) -> None:
    """Run nginx as a non daemon process using generated app config file.

    This will test the generated nginx configuration file (especially the delivery of static files).
    """
    import signal
    import threading
    from tempfile import NamedTemporaryFile

    from ..utils import browser, require_mod

    nginx_exe = which("nginx")
    if asgi:
        require_mod("uvicorn")
    else:
        require_mod("gunicorn")

    def once(m: str) -> Callable[[re.Match[str]], str]:
        done = False

        def f(r: re.Match[str]) -> str:
            nonlocal done
            if done:
                return ""
            done = True
            return m

        return f

    def get_server() -> tuple[str, str | None]:
        """parse nginx.conf file for server and host"""

        A = re.compile("access_log [^;]+;")  # remove access log entries
        L = re.compile("listen [^;]+;")  # replace listen {port}; with our port
        H = re.compile(
            r"proxy_pass\s+http://([^\s]+)/?\s*;",
        )  # find where the proxy app.sock is..
        # search for say:
        #    upstream app {
        #        server unix:{{application_dir}}/app.sock fail_timeout=0;
        #    }
        S = re.compile(r"server\s+([^{\s]+)/?.*;")

        server = nginxfile.read()
        # remove old access_log and replace listen commands
        server = A.sub("", server)
        server = L.sub(once(f"listen {port};"), server)
        # find unix socket locations
        m = S.search(server) or H.search(server)
        return server, None if not m else m.group(1)

    template: Template = get_template("nginx-app.conf", application_dir)
    server, bind = get_server()
    if not bind or not bind.startswith("unix:"):
        click.secho("can't find unix: socket entrypoint in file", err=True, fg="red")
        raise click.Abort()
    application_dir = application_dir or "."

    res = template.render(server=server)
    threads: list[threading.Thread] = []

    with NamedTemporaryFile("w") as fp:
        fp.write(res)
        fp.flush()
        url = f"http://127.0.0.1:{port}"
        click.secho(f"listening on {url}", fg="green", bold=True)

        entry = entrypoint or get_app_entrypoint(application_dir, asgi=asgi)
        app = run_app(application_dir, bind, entry, asgi=asgi, args=server_args)

        if browse:
            threads.append(browser(url))
        try:
            subprocess.run([nginx_exe, "-c", fp.name], check=False)
        finally:
            os.kill(app.pid, signal.SIGINT)
            for thrd in threads:
                thrd.join(timeout=2.0)
            # os.system("stty sane")


